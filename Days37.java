package DaysOfCoding;

public class Days37 {

    public static void main(String[] args) {
        //Operator pengisian nilai
        int a = 4;//mengisi variabel a dengan nilai 4
        int b = 2;//mengisi variabel b dengan nilai 2
        System.out.println(a);
        System.out.println(b);

        // Operator Pengisian dan Penambahan
        a += b;
        System.out.println("Hasil penambahan : " + a);

        //Operaator Pengisian dan Pengurangan
        a -= b;
        System.out.println("Hasil Pengurangan : " + a);
    }
}
