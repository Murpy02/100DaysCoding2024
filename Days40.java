package DaysOfCoding;

public class Days40 {

    public static void main(String[] args) {
        int a = 20;
        int b = 40;
        boolean c;
        //Operator Perbandingan : lebih besar dan  lebih kecil
        c = a > b;//apakah a =20 lebih besar dari b=40
        System.out.println("Hasil a > b : " + c);
        c = a < b;//apakah a =20 lebih kecil dari b=40
        System.out.println("Hasil a < b : " + c);
    }
}
