
package DaysOfCoding;


public class Days36 {
    public static void main (String[] args){
        int a = 10;
        int b = 5;
        
        //perkalian
        int perkalian = a * b;// 10 * 5 = 50
        //pembagian
        int pembagian = a / b;// 10 / 5 = 2
        //modulo
        int modulo = a % b;// 10 % 5 = 0 ( sisa pembagian 10 dibagi 5 adalah 0 )
        
        System.out.println( " Hasil Perkalian : " + perkalian);
        System.out.println( " Hasil Pembagian : " + pembagian);
        System.out.println( " Hasil Modulo : " + modulo);
                
    }
    
}
