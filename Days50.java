
package DaysOfCoding;


public class Days50 {
    public static void main(String[] args) {
       int umur = 18;
       
//       Percabangan : if- else if - else
       if ( umur >= 18 && umur <= 25){
           System.out.println("Anda masih muda");
       }else if ( umur >= 25 && umur <= 50){
           System.out.println("Anda sudah dewasa");
       } else {
           System.out.println("Pengalaman anda sudah banyak");
       }
}
}